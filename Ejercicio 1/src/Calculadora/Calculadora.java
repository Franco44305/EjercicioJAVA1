package Calculadora;

public class Calculadora
{
    private int Numero1;
    private int Numero2;

    private int Suma(int Numero1, int Numero2)
    {
        return (Numero1 + Numero2);
    }
    private int Resta(int Numero1, int Numero2)
    {
        return (Numero1 - Numero2);
    }
    private int Multiplicar(int Numero1, int Numero2)
    {
        return (Numero1 * Numero2);
    }
    private int Dividir(int Numero1, int Numero2)
    {
        return (Numero1 / Numero2);
    }
}
